export function calculateTotalEstimatedCost(spaceIds: string[]): { min: number; max: number } {
  return spaceIds.reduce(
    (acc, id) => {
      const analysis = SPACE_ANALYSIS[id]
      if (analysis) {
        acc.min += analysis.estimatedCost.min
        acc.max += analysis.estimatedCost.max
      }
      return acc
    },
    { min: 0, max: 0 }
  )
}







